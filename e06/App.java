public class App {
    public static void main(String[] args) {
        // 1. Declare boolean variables
        boolean isJavaFun = true;
        boolean isFishTasty = false;

        // 2. Perform and print logical operations
        System.out.println("isJavaFun AND isFishTasty: " + (isJavaFun && isFishTasty));
        System.out.println("isJavaFun OR isFishTasty: " + (isJavaFun || isFishTasty));
        System.out.println("NOT isJavaFun: " + (!isJavaFun));
        System.out.println("NOT isFishTasty: " + (!isFishTasty));
    }
}
